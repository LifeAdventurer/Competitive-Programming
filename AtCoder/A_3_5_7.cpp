#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    long long x[3];
    cin >> x[0] >> x[1] >> x[2];
    sort(x, x + 3);
    long long sum = x[0] + x[1] + x[2];
    if((x[0] % 2 != x[1] % 2) || (x[1] % 2 != x[2] % 2) || (sum % 3)){
      cout << "-1\n";
      continue;
    }
    sum /= 3;
    cout << ((abs(sum - x[0]) + abs(sum - x[1]) + abs(sum - x[2])) / 4) << '\n';
  }

  return 0;
}

