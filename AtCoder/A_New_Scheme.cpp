#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  bool ok = true;
  vector<int> v;
  for(int i = 0; i < 8; ++i){
    int x;
    cin >> x;
    if(x < 100 || x > 675) ok = false;
    if(x % 25 != 0) ok = false;
    v.push_back(x);
  }
  cout << (is_sorted(all(v)) && ok ? "Yes\n" : "No\n");

  return 0;
}

