int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int N, S, K;
  cin >> N >> S >> K;

  long long ans = 0;
  for(int i = 0; i < N; ++i){
    int p, q;
    cin >> p >> q;
    ans += p * q;
  }
  ans += (ans >= S ? 0 : K);
  cout << ans << '\n';

  return 0;
}

