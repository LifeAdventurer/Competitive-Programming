#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  int n;
  cin >> n;

  string s, t;
  cin >> s >> t;
  
  for(int i = 0; i < n; ++i){
    if(s[i] == '1') s[i] = 'l';
    if(s[i] == '0') s[i] = 'o';
    if(t[i] == '1') t[i] = 'l';
    if(t[i] == '0') t[i] = 'o';
  }

  cout << (s == t ? "Yes\n" : "No\n");

  return 0;
}

