#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    long long n, k;
    cin >> n >> k;
    long long cnt = 0, p = n;
    while(p){
      cnt += p % 3;
      p /= 3;
    }
    cout << (cnt <= k && k <= n && (k - cnt) % 2 == 0 ? "Yes\n" : "No\n");
  }

  return 0;
}

