#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  string s;
  cin >> n >> s;
  
  for(int i = 1; i <= n - 1; ++i){
    int cnt = 0;
    while(cnt + i < n && s[cnt] != s[cnt + i]){
      cnt++;
    }
    cout << cnt << '\n';
  }

  return 0;
}

