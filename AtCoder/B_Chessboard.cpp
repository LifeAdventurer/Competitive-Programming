#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  char c;
  int x, y;
  for(int i = 0; i < 8; ++i){
    for(int j = 0; j < 8; ++j){
      cin >> c;
      if(c == '*'){
        y = 8 - i;
        x = j + 1;
      }
    }
  }
  cout << (char)('a' + x - 1) << y << '\n';

  return 0;
}

