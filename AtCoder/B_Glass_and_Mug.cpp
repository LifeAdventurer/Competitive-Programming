#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int K, G, M;
  cin >> K >> G >> M;

  int a = G, b = M;
  while(K--){
    if(a == G) a = 0;
    else if(b == 0) b = M;
    else{
      int x = min(G - a, b);
      a += x;
      b -= x;
    }
  }
  cout << a << " " << b << '\n';

  return 0;
}

