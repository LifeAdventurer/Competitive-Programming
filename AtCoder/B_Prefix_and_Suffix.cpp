int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, m;
  cin >> n >> m;
  string s, t;
  cin >> s >> t;
  if(s == t.substr(0, n) && s == t.substr(m - n)){
    cout << "0\n";
  }
  else if(s == t.substr(0, n)){
    cout << "1\n";
  }
  else if(s == t.substr(m - n)){
    cout << "2\n";
  }
  else cout << "3\n";
  

  return 0;
}

