#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  vector<array<int, 3>> ans;
  for (int i = 0; i <= n; i++) {
    for (int j = 0; j <= n; j++) {
        for(int k = 0; k <= n; k++) {
            if (i + j + k <= n){
                ans.push_back({i, j, k});
            }
        }
    }
  }
  sort(ans.begin(), ans.end());
  for (auto [x, y, z] : ans) {
    cout << x << " " << y << " " << z << '\n';
  }

  return 0;
}

