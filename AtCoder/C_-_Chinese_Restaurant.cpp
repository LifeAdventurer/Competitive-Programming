#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  vector<int> a(n, 0);
  for(int i = 0; i < n; ++i){
    int x;
    cin >> x;
    a[(i + n - x) % n]++;
  }
  a.push_back(a[0]);
  a.push_back(a[1]);
  int ans = 0, sum;
  for(int i = 0; i < n; ++i){
    sum = 0;
    for(int j = 0; j < 3; ++j){
      sum += a[i + j];
    }
    ans = max(ans, sum);
  }
  cout << ans << '\n';

  return 0;
}

