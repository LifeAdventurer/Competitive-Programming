#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  set<int> s;
  for(int i = 0; i < n; ++i){
    int x;
    cin >> x;
    s.insert(x);
  }
  int ans = 0;
  while(n > 0){
    if(s.count(ans + 1)){
      ans++;
      n--;
    }
    else{
      if(n >= 2){
        ans++;
      }
      n -= 2;
    }
  }
  cout << ans << '\n';

  return 0;
}

