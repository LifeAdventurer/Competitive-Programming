#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  long long n, a, b;
  cin >> n >> a >> b;
  string s;
  cin >> s;

  long long ans = 1e18, cnt;
  for(int i = 0; i < n; ++i){
    cnt = i * a;
    string p = s.substr(0, i);
    string q = s.substr(i);
    q += p;
    
    for(int i = 0; i < n / 2; ++i){
      cnt += (q[i] != q[n - i - 1]) * b;
    }
    ans = min(ans, cnt);
  }

  cout << ans << '\n';

  return 0;
}

