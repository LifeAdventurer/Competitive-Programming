#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, x;
  cin >> n >> x;
  vector<int> v(n);
  set<int> s;
  for(int i = 0; i < n; ++i){
    cin >> v[i];
    s.insert(v[i]);
  }

  for(int i = 1; i < n; ++i){
    if(s.count(v[i] + x) || s.count(v[i] - x)){
      cout << "Yes\n";
      return 0;
    }
  }
  cout << "No\n";


  return 0;
}

