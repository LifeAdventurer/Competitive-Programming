#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, m;
  cin >> n >> m;
  string s;
  cin >> s;
  
  int mx = 0, ones = 0, twos = 0;
  for(int i = 0; i < n; ++i){
    if(s[i] == '0'){
      mx = max(mx, twos + max(0, ones - m));
      ones = 0;
      twos = 0;
    }
    else if(s[i] == '1'){
      ones++;
    }
    else{
      twos++;
    }
  }
  mx = max(mx, twos + max(0, ones - m));

  cout << mx << '\n';

  return 0;
}

