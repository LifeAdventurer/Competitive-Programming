#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n, k;
  cin >> n >> k;
  vector<int> v(k);
  for(int i = 0; i < k; ++i){
    cin >> v[i];
  }
  int a = 0, b = 0, ptr = k - 1, cnt = 0;
  while(n > 0){
    cnt++;
    while(ptr >= 0 && v[ptr] > n) ptr--;
    if(cnt & 1) a += v[ptr];
    else b += v[ptr];
    n -= v[ptr];
  }
  cout << a << '\n';

  return 0;
}

