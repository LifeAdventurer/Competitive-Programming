#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int N, W;
  cin >> N >> W;
  
  vector<long long> dp(W + 1, 0);
  while(N--){
    int w, v;
    cin >> w >> v;
    for(int i = W - w; i >= 0; --i){
      dp[i + w] = max(dp[i + w], dp[i] + v);
    }
  }
  cout << dp[W] << '\n';

  return 0;
}

