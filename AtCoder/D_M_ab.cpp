#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  long long n, m;
  cin >> n >> m;
  if(sqrt(m) > n){
    cout << "-1\n";
    return 0;
  }
  long long ans = 1e18;
  for(long long i = 0; i * i <= m; ++i){
    long long j = (m + i) / (i + 1);
    if(j <= n) ans = min(ans, (i + 1) * j);
  }

  cout << ans << '\n';

  return 0;
}

