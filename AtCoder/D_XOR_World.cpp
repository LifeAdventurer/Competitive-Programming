int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  long long a, b;
  cin >> a >> b;
  auto solve = [&](long long x) -> long long{
    if(x % 4 == 0) return x;
    else if(x % 4 == 1) return 1LL;
    else if(x % 4 == 2) return x + 1;
    else return 0LL;
  };
  cout << (solve(b) ^ solve(a - 1)) << '\n';

  return 0;
}

