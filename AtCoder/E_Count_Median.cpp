#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;

  vector<int> a(n), b(n);
  for(int i = 0; i < n; ++i){
    cin >> a[i] >> b[i];
  }
  sort(all(a));
  sort(all(b));

  if(n & 1){
    cout << b[n >> 1] - a[n >> 1] + 1 << '\n';
  }
  else{
    cout << b[(n >> 1) - 1] + b[n >> 1] - a[n >> 1] - a[(n >> 1) - 1] + 1 << '\n';
  }

  return 0;
}

