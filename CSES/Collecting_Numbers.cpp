#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  
  vector<int> v(n);
  for(int i = 0; i < n; ++i){
    int x;
    cin >> x;
    v[--x] = i;
  }

  int ans = 1;
  for(int i = 1; i < n; ++i){
    if(v[i] < v[i - 1]) ans++;
  }

  cout << ans << '\n';


  return 0;
}

