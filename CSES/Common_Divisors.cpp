#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;

  int mx = 0;
  vector<int> v(1e6 + 5);
  for(int i = 0; i < n; ++i){
    int a;
    cin >> a;
    mx = max(mx, a);
    v[a]++;
  }

  for(int i = mx; i >= 1; --i){
    int cnt = 0;
    for(int j = i; j <= mx; j += i){
      cnt += v[j];
    }
    if(cnt >= 2){
      cout << i << '\n';
      break;
    }
  }

  return 0;
}

