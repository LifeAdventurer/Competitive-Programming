#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

const int maxn = 1e6 + 5;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  
  vector<int> v(maxn, 1);
  
  for(int i = 2; i <= maxn; ++i){
    for(int j = i; j <= maxn; j += i){
      v[j]++;
    }
  }

  while(n--){
    int x;
    cin >> x;
    cout << v[x] << '\n';
  }

  return 0;
}

