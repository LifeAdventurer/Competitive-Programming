#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, k;
  cin >> n >> k;

  vector<int> v(n, 0);
  for(int i = 0; i < n; ++i){
    string s;
    cin >> s;
    for(int j = 0; j < (int)s[i].size(); ++j){
      v[i] += (s[j] == '1') * (1 << j);
    }
  }

  int ans = 100;
  for(int i = 0; i < n; ++i){
    for(int j = i + 1; j < n; ++j){
      ans = min(ans, (int)__builtin_popcount(v[i] ^ v[j]));
    }
  }
  cout << ans << '\n';

  return 0;
}

