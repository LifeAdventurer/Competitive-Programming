const int mod = 1000000007;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, m;
  cin >> n >> m;
  vector<vector<pair<int, int>>> G(n + 1);
  while(m--){
    int a, b, c;
    cin >> a >> b >> c;
    G[a].push_back({b, c});
  }

  vector<long long> dis(n + 1, (long long) 1e18);
  vector<int> cnt(n + 1, 0), mn(n + 1, (int) 1e9), mx(n + 1, 0);
  using T = pair<long long, int>;
  priority_queue<T, vector<T>, greater<T>> pq;
  pq.push({0, 1});
  dis[1] = mn[1] = 0;
  cnt[1] = 1;

  while(!pq.empty()){
    auto [du, u] = pq.top();
    pq.pop();
    if(du > dis[u]) continue;
    for(auto [v, w] : G[u]){
      if(dis[v] == dis[u] + w){
        cnt[v] = (cnt[v] + cnt[u]) % mod;
        mn[v] = min(mn[v], mn[u] + 1);
        mx[v] = max(mx[v], mx[u] + 1);
      }
      if(dis[v] > dis[u] + w){
        dis[v] = dis[u] + w;
        cnt[v] = cnt[u];
        mn[v] = mn[u] + 1;
        mx[v] = mx[u] + 1;
        pq.push({dis[v], v});
      }
    }
  }
  cout << dis[n] << " " << cnt[n] << " " << mn[n] << " " << mx[n] << '\n';

  return 0;
}

