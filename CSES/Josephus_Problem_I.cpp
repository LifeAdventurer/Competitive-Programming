#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  queue<int> q;
  for(int i = 1; i <= n; ++i){
    q.push(i);
  }

  for(int i = 0; i < n; ++i){
    q.push(q.front());
    q.pop();
    cout << q.front() << " \n"[i == n - 1];
    q.pop();
  }

  return 0;
}

