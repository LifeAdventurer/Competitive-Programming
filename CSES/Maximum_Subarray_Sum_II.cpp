#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, a, b;
  cin >> n >> a >> b;
  vector<long long> v(n + 1);
  for(int i = 1; i <= n; ++i){
    cin >> v[i];
  }
  partial_sum(all(v), v.begin());
  long long ans = -1e18;
  multiset<long long> m;
  for(int i = a; i <= n; ++i){
    m.insert(v[i - a]);
    ans = max(ans, v[i] - *m.begin());
    if(i >= b) m.erase(m.find(v[i - b]));
  }
  cout << ans << '\n';

  return 0;
}

