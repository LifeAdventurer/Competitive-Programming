#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, k;
  cin >> n >> k;
  
  vector<pair<int, int>> v(n);
  for(int i = 0; i < n; ++i){
    cin >> v[i].second >> v[i].first;
  }
  sort(all(v));

  multiset<int> end_time;
  while(k--){
    end_time.insert(0);
  }

  int ans = 0;
  for(int i = 0; i < n; ++i){
    auto it = end_time.upper_bound(v[i].second);
    if(it == end_time.begin()) continue;
    end_time.erase(--it);
    end_time.insert(v[i].first);
    ++ans;
  }

  cout << ans << '\n';

  return 0;
}

