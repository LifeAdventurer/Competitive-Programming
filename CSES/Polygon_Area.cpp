#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  vector<long long> x(n + 1), y(n + 1);

  for(int i = 0; i < n; ++i){
    cin >> x[i] >> y[i];
  }
  x[n] = x[0];
  y[n] = y[0];

  long long ans = 0;
  for(int i = 0; i < n; ++i){
    ans += x[i] * y[i + 1];
    ans -= y[i] * x[i + 1];
  }

  cout << abs(ans) << '\n';

  return 0;
}

