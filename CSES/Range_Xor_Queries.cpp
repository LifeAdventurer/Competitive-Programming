#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, q;
  cin >> n >> q;
  vector<int> pre_Xor(n + 1);
  for(int i = 1; i <= n; ++i){
    int x;
    cin >> x;
    pre_Xor[i] = (pre_Xor[i - 1] ^ x);
  }
  while(q--){
    int a, b;
    cin >> a >> b;
    cout << (pre_Xor[b] ^ pre_Xor[a - 1]) << '\n';
  }

  return 0;
}

