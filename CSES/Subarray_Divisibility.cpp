#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int n;

int mod(int x){
  return (x % n + n) % n;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  cin >> n;

  vector<int> cnt(n, 0);
  cnt[0] = 1;
  long long ans = 0, pre = 0;
  for(int i = 0; i < n; ++i){
    int a;
    cin >> a;
    pre = mod(pre + a);
    ans += cnt[pre];
    cnt[pre]++;
  }
  
  cout << ans << '\n';

  return 0;
}

