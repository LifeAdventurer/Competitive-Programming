#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

void solve(int n, int a, int b, int c){
  if(n == 1){
    cout << a << " " << c << '\n';
  }
  else{
    solve(n - 1, a, c, b);
    cout << a << " " << c << '\n';
    solve(n - 1, b, a, c);
  }
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  int moves = (1 << n) - 1;
  cout << moves << '\n';
  solve(n, 1, 2, 3);

  return 0;
}

