#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;

  multiset<int> ms;
  for(int i = 0; i < n; ++i){
    int x;
    cin >> x;
    auto it = ms.upper_bound(x);
    if(it != ms.end()){
      ms.erase(it);
    }
    ms.insert(x);
  }

  cout << ms.size() << '\n';

  return 0;
}

