#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int x, n;
  cin >> x >> n;
  
  set<int> pos;
  multiset<int> len;

  pos.insert(0);
  pos.insert(x);
  len.insert(x);

  for(int i = 0; i < n; ++i){
    int p;
    cin >> p;

    auto r = pos.upper_bound(p);
    auto l = prev(r);

    len.erase(len.find(*r - *l));
    len.insert(*r - p);
    len.insert(p - *l);

    pos.insert(p);

    cout << *--len.end() << " \n"[i == n - 1];
  }

  return 0;
}

