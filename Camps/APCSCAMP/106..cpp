#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int m, n;
  cin >> m >> n;

  vector<int> w(n), p(n);

  for(int i = 0; i < n; ++i){
    cin >> w[i] >> p[i];
  }

  int k = 1 << n, ans = 0;
  while(k--){
    int q = k, sum_w = 0, sum_p = 0;
    
    for(int i = 0; i < n; ++i){
      if(q & 1){
        sum_w += w[i];
        sum_p += p[i];
      }
      q >>= 1;
    }
    if(sum_w <= m) ans = max(ans, sum_p);
  }

  cout << ans << '\n';

  return 0;
}

