#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

bool cmp(pair<int, int> a, pair<int, int> b){
  return a.second < b.second;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;

  vector<pair<int, int>> v;

  for(int i = 0; i < n; ++i){
    int a, b;
    cin >> a >> b;
    v.emplace_back(a, b);
  }

  sort(all(v), cmp);

  int ans = 0, end = 0;

  for(auto x : v){
    if(x.first >= end){
      ans++;
      end = x.second;
    }
  }

  cout << ans << '\n';

  return 0;
}

