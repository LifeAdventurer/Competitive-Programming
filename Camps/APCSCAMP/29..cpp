#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  
  vector<int> v(n);

  for(int i = 0; i < n; ++i){
    cin >> v[i];
  }

  int ans = 0;

  vector<int> dp(n);

  for(int i = 0; i < n; ++i){
    int mx = 0;
    for(int j = 0; j < i; ++j){
      if(v[j] < v[i]) mx = max(mx, dp[j]);
    }
    dp[i] = mx + 1;
    ans = max(ans, dp[i]);
  }

  cout << ans << '\n';

  return 0;
}

