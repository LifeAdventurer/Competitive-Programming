#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, k;
  cin >> n >> k;

  vector<int> v(n);
  vector<long long> dp(n, 1e18);

  for(int i = 0; i < n; ++i){
    cin >> v[i];
  }

  dp[0] = 0;
  for(int i = 1; i < n; ++i){
    for(int j = max(0, i - k); j < i; ++j){
      dp[i] = min(dp[i], dp[j] + abs(v[i] - v[j]));
    }
  }

  cout << dp[n - 1] << '\n';

  return 0;
}

