#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, m;
  cin >> n >> m;

  vector<vector<long long>> dp(n, vector<long long>(m));

  for(int i = 0; i < n; ++i){
    for(int j = 0; j < m; ++j){
      int x;
      cin >> x;
      if(!i && !j) dp[0][0] = x;
      if(i) dp[i][j] = max(dp[i][j], dp[i - 1][j] + x);
      if(j) dp[i][j] = max(dp[i][j], dp[i][j - 1] + x);
    }
  }

  cout << dp[n - 1][m - 1] << '\n';

  return 0;
}

