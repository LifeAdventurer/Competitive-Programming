#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define u_ying_u_with_whale  ios::sync_with_stdio(false);cin.tie(0);cout.tie(0)

int main() {
	
	u_ying_u_with_whale;
	int n;
	string s;
	cin >> n;
	while(n--){
		cin >> s;
		stack<char> st;
		for(char c: s){
			if(c == '('){
				st.push(c);
			}
			if(c == ')'){
				if(st.empty() || st.top() != '('){
					st.push(c);
					break;
				}
				else{
					st.pop();
				}
			} 
		}
		cout << (st.empty() ? "Y\n" : "N\n");
	} 
}
