#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define u_ying_u_with_whale  ios::sync_with_stdio(false);cin.tie(0)

int main() {
	
	u_ying_u_with_whale;
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		if(n % 4 != 0){
			cout << "NO\n";
			continue;
		}
		else{
			cout << "YES\n";
			for(int i = 1; i <= n / 2; ++i){
				cout << i * 2 << " ";
			}
			for(int i = 1; i <= n / 4; ++i){
				cout << i * 2 - 1 << " ";
			}
			for(int i = n / 4 + 1; i <= n / 2; ++i){
				cout << i * 2 + 1 << " \n"[i == n / 2];
			}
		}
	}
} 

