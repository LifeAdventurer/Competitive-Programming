#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  vector<int> v(n);
  for(int i = 0; i < n; ++i){
    cin >> v[i];
  }
  vector<vector<int>> dp(2, vector<int>(2));
  for(int i = 0; i < n; ++i){
    dp[1][0] = max(dp[0][1], dp[0][0] + v[i]);
    dp[1][1] = dp[0][0] + v[i] * 2;
    dp[0][0] = dp[1][0];
    dp[0][1] = dp[1][1]; 
  }

  cout << max(dp[0][0], dp[0][1]) << '\n';

  return 0;
}

