#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int a, b;
  cin >> a >> b;
  stack<int> st;
  st.push(b);
  while(b > a){
    if(b % 10 == 1){
      b /= 10;
      st.push(b);
    }
    else if(b % 2 == 0){
      b /= 2;
      st.push(b);
    }
    else break;
  }
  if(a != b) cout << "NO\n";
  else{
    cout << "YES\n" << st.size() << '\n';
    while(!st.empty()){
      cout << st.top() << " ";
      st.pop();
    }
    cout << '\n';
  }

  return 0;
}

