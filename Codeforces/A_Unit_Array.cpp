#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    int neg = 0, pos = 0;
    while(n--){
      int x;
      cin >> x;
      if(x == -1) neg++;
      else pos++;
    }
    int ans = 0;
    while(pos < neg || (neg & 1)){
      ans++;
      pos++;
      neg--;
    }
    cout << ans << '\n';
  }

  return 0;
}

