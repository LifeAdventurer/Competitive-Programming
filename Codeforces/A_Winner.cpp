#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  int mx = 0;
  vector<string> v(n);
  vector<int> a(n);
  map<string, int> m, m1;
  for(int i = 0; i < n; ++i){
    cin >> v[i] >> a[i];
    m[v[i]] += a[i];
  }
  for(int i = 0; i < n; ++i){
    mx = max(mx, m[v[i]]);
  }
  int p = 0;
  while(1){
    m1[v[p]] += a[p];
    if(m[v[p]] == mx && m1[v[p]] >= mx) break;
    p++;
  } 
  cout << v[p] << '\n';

  return 0;
}

