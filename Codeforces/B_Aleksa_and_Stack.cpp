#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    vector<int> v = {2, 3};
    int p = 4;
    for(int i = 2; i < n; ++i){
      int a = p * 3, b = v[i - 1] + v[i - 2];
      while(a > b && a % b == 0){
        p++;
        a = p * 3;
      }
      v.push_back(p);
      p++;
    }
    for(int i = 0; i < n; ++i){
      cout << v[i] << " \n"[i == n - 1];
    }
  }

  return 0;
}

