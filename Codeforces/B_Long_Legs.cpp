#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int a, b;
    cin >> a >> b;
    
    int ans = 2.1e9;
    for(int i = 1; i <= 1e5; ++i){
      ans = min(ans, i - 1 + (a + i - 1)/ i + (b + i - 1) / i);
    }
    cout << ans << '\n';
  }

  return 0;
}

