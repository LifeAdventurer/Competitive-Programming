#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int a, b, c;
    cin >> a >> b >> c;
    if((a + c) < b * 2){
      if((b * 2 - a) % c == 0){
        cout << "YES\n";
      }
      else if((b * 2 - c) % a == 0){
        cout << "YES\n";
      }
      else cout << "NO\n";
    }
    else if((a + c) > b * 2){
      cout << ((a + c) % (b * 2) == 0 ? "YES\n" : "NO\n");
    }
    else cout << "YES\n";
  }

  return 0;
}

