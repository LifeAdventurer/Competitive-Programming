#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  
  while(t--){
    int n, s, r;
    cin >> n >> s >> r;
    cout << s - r << " ";
    int p = r % (n - 1);
    for(int i = 0; i < p; ++i){
      cout << r / (n - 1) + 1 << " ";
    }
    for(int i = 0; i < n - 1 - p; ++i){
      cout << r / (n - 1) << " \n"[i == n - 2 - p];
    }
  }

  return 0;
}

