#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    int ans = 0, k = 0;
    for(int i = 0; i < n; ++i){
      int a, b;
      cin >> a >> b;
      if(b > k && a <= 10){
        k = b;
        ans = i + 1;
      }
    }
    cout << ans << '\n';
  }

  return 0;
}

