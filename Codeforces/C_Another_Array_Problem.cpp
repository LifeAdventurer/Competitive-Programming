#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    vector<long long> v(n);
    for(int i = 0; i < n; ++i){
      cin >> v[i];
    }
  
    long long ans;
    if(n == 2) ans = max(v[0] + v[1], 2 * abs(v[0] - v[1]));
    else if(n == 3){
      ans = max(v[0] + v[1] + v[2], 3 * max({v[0], v[2], abs(v[0] - v[1]), abs(v[1] - v[2])}));
    } 
    else{
      ans = *max_element(all(v)) * n;
    }
    
    cout << ans << '\n';
  }

  return 0;
}

