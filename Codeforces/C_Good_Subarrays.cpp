#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    string s;
    long long ans = 0, cur = 0;
    map<int, int> m;
    m[0] = 1;
    for(int i = 1; i <= n; ++i){
      char c;
      cin >> c;
      cur += c - '0';
      ans += m[cur - i]++;
    }
    cout << ans << '\n';
  }

  return 0;
}

