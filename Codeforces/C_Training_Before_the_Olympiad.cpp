#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    vector<int> v(n);
    for (int i = 0; i < n; i++) {
      cin >> v[i];
    }
    int odd = v[0] & 1;
    long long pref = v[0];
    cout << v[0];
    for (int i = 1; i < n; i++) {
      pref += v[i];
      odd += v[i] & 1;
      int minus = odd / 3 + (odd % 3 == 1 ? 1 : 0);
      cout << " " << pref - minus;
    }
    cout << '\n';
  }

  return 0;
}

