#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

long long fastpow(long long x, long long y, int mod){
  x %= mod;
  long long res = 1;
  while(y){
    if(y & 1) res = res * x % mod;
    y >>= 1;
    x = x * x % mod;
  }
  return res;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    string s;
    cin >> s;
    vector<int> cnt(26, 0);
    for(int i = 0; i < n; ++i){
      cnt[s[i] - 'a']++;
    }
    int ans = 0;
    vector<int> cnt2(26, 0);
    for(int i = 0; i < n - 1; ++i){
      cnt2[s[i] - 'a']++;
      int sum1 = 0, sum2 = 0;
      for(int j = 0; j < 26; ++j){
        sum1 += (cnt2[j] > 0);
        sum2 += ((cnt[j] - cnt2[j]) > 0);
      }
      ans = max(ans, sum1 + sum2);
    }
    cout << ans << '\n';
  }

  return 0;
}

