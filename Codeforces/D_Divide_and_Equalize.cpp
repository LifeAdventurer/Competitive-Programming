#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    map<int, int> cnt;
    for(int i = 0; i < n; ++i){
      int x;
      cin >> x;
      for(int j = 2; j * j <= x; ++j){
        while(x % j == 0){
          cnt[j]++;
          x /= j;
        }
      }
      if(x > 1) cnt[x]++;
    } 
    bool ok = true;
    for(auto [x, y] : cnt){
      ok &= (y % n == 0);
    }
    cout << (ok ? "YES\n" : "NO\n");
  }

  return 0;
}

