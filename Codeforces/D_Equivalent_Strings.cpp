#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  string s, t;
  cin >> s >> t;

  auto Solve = [&](auto Solve, string s) -> string{
    if(s.size() & 1) return s;

    string l = Solve(Solve, s.substr(0, s.size() / 2));
    string r = Solve(Solve, s.substr(s.size() / 2));

    return (l > r ? r + l : l + r);
  };

  s = Solve(Solve, s);
  t = Solve(Solve, t);
  cout << (s == t ? "YES\n" : "NO\n");

  return 0;
}

