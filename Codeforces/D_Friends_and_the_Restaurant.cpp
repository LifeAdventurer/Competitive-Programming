#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int t;
  cin >> t;
  while(t--){
    int n;
    cin >> n;
    vector<int> v(n);
    for(int i = 0; i < n; ++i){
      cin >> v[i];
    }
    for(int i = 0; i < n; ++i){
      int x;
      cin >> x;
      v[i] = x - v[i];
    }
    sort(all(v));
    
    int l = 0, r = n - 1, ans = 0;
    while(l < r){
      if(v[r] + v[l] >= 0){
        l++;
        r--;
        ans++;
      }
      else{
        l++;
      }
    }
    cout << ans << '\n';
  }

  return 0;
}

