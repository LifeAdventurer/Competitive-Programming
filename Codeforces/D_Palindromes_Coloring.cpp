#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  while(t--){
    int n, k;
    string s;
    cin >> n >> k >> s;
    vector<int> cnt(26, 0);
    for(int i = 0; i < n; ++i){
      cnt[s[i] - 'a']++;
    }

    int odd = 0, pairs = 0;
    for(int i = 0; i < 26; ++i){
      pairs += cnt[i] / 2;
      odd += cnt[i] % 2;
    }  
    cout << (pairs / k) * 2 + (odd + (pairs % k) * 2 >= k ? 1 : 0) << '\n';
  }

  return 0;
}

