#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  for(int i = 1; ; i++){
    for(int j = 1; j < i; j++){
      long long x, y;
      cout << "? " << i << " " << j << endl;
      cin >> x;
      cout << "? " << j << " " << i << endl;
      cin >> y;
      if(x == -1 || y == -1){
        cout << "! " << i - 1 << endl;
        return 0;
      }
      if (x != y) {
        cout << "! " << x + y << endl;
        return 0;
      }
    }
  }

  return 0;
}

