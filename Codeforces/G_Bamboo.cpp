#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  long long n, c, t, k1, k2, k3;
  cin >> n >> c >> t >> k1 >> k2 >> k3;
  long long ans = 0;
  for(int i = 0; i <= t; ++i){
    for(int j = 0; j <= t; ++j){
      for(int k = 0; k <= j; ++k){
        long long sum = n - k3 * i + k1 * k + k2 * (j - k);
        if(sum <= c && sum > ans) ans = sum;
      }
    }
  }

  cout << ans << '\n';

  return 0;
}

