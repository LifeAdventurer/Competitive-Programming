#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  long long l, n;
  cin >> l >> n;
  vector<long long> v(n);
  for(int i = 0; i < n; ++i){
    cin >> v[i];
  }
  v.push_back(0LL);
  v.push_back(l + 1);
  sort(all(v));
  long long ans = 0;
  for(int i = 1; i < n + 2; ++i){
    ans += (v[i] - v[i - 1] - 1) / 15;
  }
  cout << ans << '\n';

  return 0;
}

