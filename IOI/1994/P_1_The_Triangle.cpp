#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  vector<vector<int>> dp(n);
  for(int i = 0; i < n; ++i){
    for(int j = 0; j < i + 1; ++j){
      int x;
      cin >> x;
      dp[i].push_back(x);
    }
  }

  for(int i = n - 1; i > 0; --i){
    for(int j = 0; j < i; ++j){
      dp[i - 1][j] += max(dp[i][j], dp[i][j + 1]);
    }
  }

  cout << dp[0][0] << '\n';

  return 0;
}

