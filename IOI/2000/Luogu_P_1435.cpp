#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  string s;
  cin >> s;
  int n = s.size();
  string t = s;
  reverse(t.begin(), t.end());
  vector<vector<int>> dp(n + 1, vector<int>(n + 1));
  for(int i = 1; i <= n; ++i){
    for(int j = 1; j <= n; ++j){
      if(s[i - 1] == t[j - 1]) dp[i][j] = dp[i - 1][j - 1] + 1;
      else dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
    }
  }
  cout << n - dp[n][n] << '\n';

  return 0;
}

