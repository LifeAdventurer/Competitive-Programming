#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int t;
  cin >> t;
  for(int i = 1; i <= t; ++i){
    int n;
    cin >> n;

    set<int> s;
    for(int i = 0; i < n; ++i){
      int x;
      cin >> x;
      if(s.find(x) == s.end()) s.insert(x);
      else s.erase(x);
    }

    cout << "Case #" << i << ": " << *s.begin() << '\n';
  }

  return 0;
}

