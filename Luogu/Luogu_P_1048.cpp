#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int T, M;
  cin >> T >> M;
  vector<int> dp(T + 1);
  for(int i = 0; i < M; ++i){
    int v, w;
    cin >> v >> w;
    for(int j = T; j >= v; --j){
      dp[j] = max(dp[j], dp[j - v] + w);
    }
  }

  cout << dp[T] << '\n';

  return 0;
}

