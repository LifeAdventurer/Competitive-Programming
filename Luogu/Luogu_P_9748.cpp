int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  int cnt = 1, ans = 0;
  while(n > 0){
    if(!ans && n % 3 == 1) ans = cnt;
    n -= (n + 2) / 3;
    cnt++;
  }
  cout << cnt - 1 << " " << ans << '\n';

  return 0;
}

