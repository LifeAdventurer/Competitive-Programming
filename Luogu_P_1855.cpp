#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  int n, M, T;
  cin >> n >> M >> T;
  vector<vector<int>> dp(M + 1, vector<int>(T + 1));
  for(int i = 0; i < n; ++i){
    int m, t;
    cin >> m >> t;
    for(int i = M; i >= m; --i){
      for(int j = T; j >= t; --j){
        dp[i][j] = max(dp[i][j], dp[i - m][j - t] + 1);
      }
    }
  }
  cout << dp[M][T] << '\n';

  return 0;
}

