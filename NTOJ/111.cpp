#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int x, y;
  cin >> x >> y;
  long long p = 1;
  while(p % x){
    p += 1;
    if(p % x == 0) break;
    p <<= 1;
  }
  cout << (p % y == 0 ? "right\n" : "left\n");

  return 0;
}

