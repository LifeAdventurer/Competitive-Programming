#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0);
	long long a, b, mod;
	cin >> a >> b >> mod;
	long long ans = 1;
	while(b > 0){
		if(b & 1) ans = (ans * a) % mod;
		a = (a * a) % mod;
		b >>= 1;
	}
	cout << ans << '\n';

	return 0;
}

