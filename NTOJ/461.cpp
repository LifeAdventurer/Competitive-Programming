#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int ans = 0;
  double d;

  cin >> d;

  for(int i = 0; i < 720; ++i){
    double x = i / 60 * 30 + i % 60 * 0.5, y = i % 60 * 6.0;
    double k = abs(x - y);
    if(k == d || (360 - k) == d) ans++;
  }

  cout << ans * 2 << '\n';

  return 0;
}

