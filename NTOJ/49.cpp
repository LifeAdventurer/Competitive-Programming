#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  string s;
  char c;
  long long a, b;
  while(cin >> s >> c >> a >> c >> b >> c){
    if(s == "GCD"){
      cout << gcd(a, b);
    }
    else if(s == "LCM"){
      cout << lcm(a, b);
    }
    else if(s == "ADD"){
      cout << a + b;
    }
    else if(s == "SUB"){
      cout << a - b;
    }
    else if(s == "MUL"){
      cout << a * b;
    }
    else cout << a / b;
    cout << '\n';
  }

  return 0;
}

