#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

bool is_prime(int p){
  for(int i = 2; i * i <= p; ++i){
    if(p % i == 0) return false;
  }
  return true;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  int k;
  cin >> k;
  int cnt = 0, p = 2;
  while(cnt <= k){
    if(is_prime(p)) cnt++;
    p++;
  }
  cout << p - 2 << '\n';

  return 0;
}

