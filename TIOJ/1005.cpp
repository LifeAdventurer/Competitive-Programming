#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  while(cin >> n && n){
    vector<int> v(n);
    for(int i = 0; i < n; ++i){
      cin >> v[i];
    }

    double cnt = 0;
    for(int i = 0; i < n; ++i){
      for(int j = i + 1; j < n; ++j){
        cnt += (__gcd(v[i], v[j]) == 1);
      }
    }
    if(!cnt) cout << "No estimate for this data set.\n";
    else cout << fixed << setprecision(6) << sqrt((3.0 * n * (n - 1)) / cnt) << '\n';
  }

  return 0;
}

