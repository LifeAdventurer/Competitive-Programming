#include <bits/stdc++.h>

using namespace std;

#ifdef LOCAL
#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n, m;
  cin >> n >> m;
  vector<long long> dp(m + 1, 0);
  dp[0] = 1;
  for(int i = 1; i <= m; ++i){
    dp[i] = dp[i - 1] << 1;
    if(i == n + 1) dp[i]--;
    else if(i > n + 1) dp[i] -= dp[i - n - 2];
  }
  cout << dp[m] << '\n';

  return 0;
}

