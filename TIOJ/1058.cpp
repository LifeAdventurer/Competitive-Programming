int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  string A, B;
  cin >> A >> B;
  long double a = stold(A), b = stold(B);
  if(a > b){
    cout << A << '\n';
    return 0;
  }
  if(b > a){
    cout << B << '\n';
    return 0;
  }
  bool ok = true;
  for(int i = 0; i < min(A.size(), B.size()); ++i){
    if(B[i] > A[i]) ok = false;
  }
  cout << (ok ? A : B) << '\n';

  return 0;
}

