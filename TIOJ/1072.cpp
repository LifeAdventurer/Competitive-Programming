#include "algo/debug.h"
#else
#define debug(...) 42
#endif

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int N;
  while(cin >> N && N){
    vector<pair<int, int>> v(N);
    for(int i = 0; i < N; ++i){
      cin >> v[i].second >> v[i].first;
    }
    sort(v.begin(), v.end(), greater<pair<int, int>>());
    int ans = 0, cur = 0;
    for(int i = 0; i < N; ++i){
      cur += v[i].second;
      ans = max(ans, cur + v[i].first);
    }
    cout << ans << '\n';
  }

  return 0;
}

