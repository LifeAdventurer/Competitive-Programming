#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;

  vector<int> dp;
  for(int i = 0; i < n; ++i){
    int x;
    cin >> x;
    auto it = lower_bound(all(dp), x);
    if(it == dp.end()) dp.push_back(x);
    else *it = x; 
  }
  cout << dp.size() << '\n';

  return 0;
}

