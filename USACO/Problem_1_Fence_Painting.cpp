#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

void setIO(string s) {
	freopen((s + ".in").c_str(), "r", stdin);
	freopen((s + ".out").c_str(), "w", stdout);
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  setIO("paint");
  
  int a, b, c, d;
  cin >> a >> b >> c >> d;
  cout << b - a + d - c - max(min(b, d) - max(a, c), 0) << '\n';

  return 0;
}

