#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  string s;
  cin >> n >> s;
  vector<int> v(n);
  
  int f = n, l = 0;
  for(int i = 0; i < n; ++i){
    cin >> v[i];
    if(s[i] == s[0]) l = i + 1;
    else f = min(f, i + 1);
  }
  int ans = (v[0] >= f || v[0] >= l);
  for(int i = 1; i < f - 1; ++i){
    ans += (v[i] >= f);
  }

  cout << ans << '\n';

  return 0;
}

