#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

long long fastpow(long long x, long long y, int mod){
  x %= mod;
  long long res = 1;
  while(y){
    if(y & 1) res = res * x % mod;
    y >>= 1;
    x = x * x % mod;
  }
  return res;
}

void setIO(string s) {
  freopen((s + ".in").c_str(), "r", stdin);
  freopen((s + ".out").c_str(), "w", stdout);
}

vector<int> c(3), m(3);

void pour(int i, int j){
  int amt = min(m[i], c[j] - m[j]);
  m[i] -= amt;
  m[j] += amt;
  return;
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  setIO("mixmilk");

  for(int i = 0; i < 3; ++i){
    cin >> c[i] >> m[i];
  }

  for(int i = 0; i < 100; ++i){
    pour(i % 3, (i + 1) % 3);
  }

  for(int i = 0; i < 3; ++i) cout << m[i] << '\n';

  return 0;
}

