#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

long long fastpow(long long x, long long y, int mod){
  x %= mod;
  long long res = 1;
  while(y){
    if(y & 1) res = res * x % mod;
    y >>= 1;
    x = x * x % mod;
  }
  return res;
}

void setIO(string s) {
  freopen((s + ".in").c_str(), "r", stdin);
  freopen((s + ".out").c_str(), "w", stdout);
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  setIO("herding");

  int a, b, c;
  cin >> a >> b >> c;

  int x = b - a - 1;
  int y = c - b - 1;
  if(!x && !y) cout << 0 << '\n';
  else if(x == 1 || y == 1) cout << 1 << '\n';
  else cout << 2 << '\n';
  cout << max(x, y) << '\n';

  return 0;
}

