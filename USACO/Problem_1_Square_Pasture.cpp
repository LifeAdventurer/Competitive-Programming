#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()
#define sq(x) (x) * (x)

long long fastpow(long long x, long long y, int mod){
  x %= mod;
  long long res = 1;
  while(y){
    if(y & 1) res = res * x % mod;
    y >>= 1;
    x = x * x % mod;
  }
  return res;
}

void setIO(string s) {
  freopen((s + ".in").c_str(), "r", stdin);
  freopen((s + ".out").c_str(), "w", stdout);
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  setIO("square");

  vector<int> x, y;
  for(int i = 0; i < 8; ++i){
    int a;
    cin >> a;
    if(i & 1) y.push_back(a);
    else x.push_back(a);
  } 

  cout << sq(max(*max_element(all(x)) - *min_element(all(x)), *max_element(all(y)) - *min_element(all(y)))) << '\n';

  return 0;
}

