#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

long long fastpow(long long x, long long y, int mod){
  x %= mod;
  long long res = 1;
  while(y){
    if(y & 1) res = res * x % mod;
    y >>= 1;
    x = x * x % mod;
  }
  return res;
}

void setIO(string s) {
  freopen((s + ".in").c_str(), "r", stdin);
  freopen((s + ".out").c_str(), "w", stdout);
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  setIO("shuffle");

  int n;
  cin >> n;

  vector<int> a(n), b(n);
  for(int i = 0; i < n; ++i){
    cin >> a[i];
  }
  for(int i = 0; i < n; ++i){
    cin >> b[i];
  }

  for(int i = 0; i < 3; ++i){
    vector<int> t = b;
    for(int j = 0; j < n; ++j){
      b[j] = t[a[j] - 1];
    }
  }

  for(int i = 0; i < n; ++i){
    cout << b[i] << '\n';
  }

  return 0;
}

