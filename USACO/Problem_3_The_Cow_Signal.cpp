#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

long long fastpow(long long x, long long y, int mod){
  x %= mod;
  long long res = 1;
  while(y){
    if(y & 1) res = res * x % mod;
    y >>= 1;
    x = x * x % mod;
  }
  return res;
}

void setIO(string s) {
  freopen((s + ".in").c_str(), "r", stdin);
  freopen((s + ".out").c_str(), "w", stdout);
}

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);

  setIO("cowsignal");

  int n, m, k;
  cin >> n >> m >> k;

  vector<vector<char>> mp(n, vector<char>(m));
  for(int i = 0; i < n; ++i){
    for(int j = 0; j < m; ++j){
      cin >> mp[i][j];
    }
  }

  for(int i = 0; i < n; ++i){
    for(int j = 0; j < k; ++j){
      for(int l = 0; l < m; ++l){
        for(int p = 0; p < k; ++p){
          cout << mp[i][l];
        }
      }
      cout << '\n';
    }
  }

  return 0;
}

