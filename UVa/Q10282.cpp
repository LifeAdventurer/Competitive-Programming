#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  string s;
  map<string, string> m;
  while(getline(cin, s) && s != ""){
    string s1 = "", s2 = "";
    int i;
    for(i = 0; s[i] != ' '; ++i){
      s1 += s[i];
    }
    ++i;
    for(int j = i; j < s.size(); ++j){
      s2 += s[j];
    }
    m[s2] = s1;
  }
  while(cin >> s){
    cout << (m.count(s) ? m[s] : "eh") << '\n';
  }

  return 0;
}

