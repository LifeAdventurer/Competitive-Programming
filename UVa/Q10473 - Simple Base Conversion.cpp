#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  string s;
  while(cin >> s){
    if(s == "-1") break;
    int n; 
    if(s[0] == '0' && s[1] == 'x'){
      s = s.substr(2);
      n = s.size();
      cerr << n;
      int ans = 0;
      for(char c : s){
        ans += (c - '0') * (16, n);
        n--;
      }
      cout << ans << '\n';
    }
    else{
      cout << "0x";
      n = stoi(s);
      while(true){
        break;
      }
    }
  }

  return 0;
}

