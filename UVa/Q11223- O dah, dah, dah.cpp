#include <bits/stdc++.h>

using namespace std;

#define u_ying_u_with_whale  ios::sync_with_stdio(false);cin.tie(0)

int main() {
	
	u_ying_u_with_whale;
	int t, cases = 1;
	cin >> t;
	string l, s;
	getline(cin, l);
	map<string, char> m = {{".-", 'A'}, {".---", 'J'}, {"...", 'S'}, {".----", '1'}, {".-.-.-", '.'}, {"---...", ':'}, 
						{"-...", 'B'}, {"-.-", 'K'}, {"-", 'T'}, {"..---", '2'}, {"--..--", ','}, {"-.-.-.", ';'}, 
						{"-.-.", 'C'}, {".-..", 'L'}, {"..-", 'U'}, {"...--", '3'}, {"..--..", '?'}, {"-...-", '='}, 
						{"-..", 'D'}, {"--", 'M'}, {"...-", 'V'}, {"....-", '4'}, {".----.", '\''}, {".-.-.", '+'}, 
						{".", 'E'}, {"-.", 'N'}, {".--", 'W'}, {".....", '5'}, {"-.-.--", '!'}, {"-....-", '-'}, 
						{"..-.", 'F'}, {"---", 'O'}, {"-..-", 'X'}, {"-....", '6'}, {"-..-.", '/'}, {"..--.-", '_'}, 
						{"--.", 'G'}, {".--.", 'P'}, {"-.--", 'Y'}, {"--...", '7'}, {"-.--.", '('}, {".-..-.", '"'}, 
						{"....", 'H'}, {"--.-", 'Q'}, {"--..", 'Z'}, {"---..", '8'}, {"-.--.-", ')'}, {".--.-.", '@'}, 
						{"..", 'I'}, {".-.", 'R'}, {"-----", '0'}, {"----.", '9'}, {".-...", '&'}};
	while(t--){
		getline(cin, l);
		s = "";
		l += ' ';
		if(cases != 1) cout << "\n";
		cout << "Message #" << cases << '\n';
		for(int i = 0; i < l.size(); ++i){
			if(l[i] == ' '){
				cout << m[s];
				if(l[i + 1] == ' '){
					cout << " ";
					++i; 
				}
				s = "";
			}
			else{
				s += l[i];
			}
		}
		cout << '\n';
		++cases; 
	}
} 

