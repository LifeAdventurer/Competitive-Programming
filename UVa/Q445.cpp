#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  string s;

  while(cin >> s){
    int n = 0;
    for(char c : s){
      if(c == '!') cout << '\n';
      else if(c == 'b'){
        for(int i = 0; i < n; ++i){
          cout << " ";
        }
        n = 0;
      }
      else if(c >= '1' && c <= '9'){
        n += c - '0';
      }
      else{
        for(int i = 0; i < n; ++i){
          cout << c;
        }
        n = 0;
      }
    }
    cout << '\n';
  }

  return 0;
}

