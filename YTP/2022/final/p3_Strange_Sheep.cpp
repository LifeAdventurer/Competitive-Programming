#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;
  vector<int> v(n);
  for(int i = 0; i < n; ++i){
    cin >> v[i];
  }
  int ans = 0, p = 1, l = 0;
  for(int i = 0; i < n; ++i){
    if(v[i] < p){
      l += p - v[i];
      p = v[i];
    }
    p++;
    ans = max(ans, i - l + 1);
  }
  cout << ans << '\n';

  return 0;
}

