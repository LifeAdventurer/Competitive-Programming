#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;

  vector<pair<int, int>> v(n);

  for(int i = 0; i < n; ++i){
    cin >> v[i].first >> v[i].second;
  }

  sort(all(v));

  int ans = 0, p = v[0].first;
  for(auto x : v){
    if(x.first <= p){
      ans += max(0, x.second - p);
    }
    else{
      ans += x.second - x.first;
    }
    p = max(p, x.second);
  }

  cout << ans << '\n';

  return 0;
}

