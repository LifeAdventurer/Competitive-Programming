#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define u_ying_u_with_whale  ios::sync_with_stdio(false);cin.tie(0)

int dp[1000005];

bool prime(int x){
	if(x == 1){
		return false;
	}
	else{
		for(int i = 2; i <= sqrt(x); ++i){
			if(x % i == 0){
				return false;
			}
		}
		return true;
	}
}

int main() {
	
	u_ying_u_with_whale;
	for(int i = 1; i < 1000000; ++i){
		int a = i, sum = 0, p = 0;
		if(prime(a)){			
			while(a > 0){
				sum += a%10;
				a /= 10;
			}
			if(prime(sum)){
				p = 1;
			}			
		}
		dp[i] = dp[i-1] + p;
	}	
	int n;
	cin >> n;
	while(n--){
		int t1, t2;
		cin >> t1 >> t2;
		cout << dp[t2] - dp[t1-1] << '\n';
	}
} 

