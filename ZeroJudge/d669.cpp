#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int a, b, c, d;
  while(cin >> a >> b >> c >> d){
    if(a == 0 && b == 0 && c == 0 && d == 0) break;

    int p = a * 60 + b;
    int q = c * 60 + d;

    if(q < p) cout << 1440 - p + q << '\n';
    else cout << q - p << '\n';
  }


  return 0;
}

