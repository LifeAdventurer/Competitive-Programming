#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define u_ying_u_with_whale  ios::sync_with_stdio(false);cin.tie(0)

int main() {
	
	u_ying_u_with_whale;
	int a1, b1, c1;
	int a2, b2, c2;
	int n;
	cin >> a1 >> b1 >> c1;
	cin >> a2 >> b2 >> c2;
	cin >> n;
	int ans = -1e9, sum;
	for(int i=0; i<=n; ++i){
		sum = (a1*i*i + b1*i + c1 + a2*(n-i)*(n-i) + b2*(n-i) + c2);
		ans = max(ans, sum);
	}
	cout << ans << '\n';
} 

