#pragma GCC optimize("Ofast,unroll-loops")
#include <bits/stdc++.h>

using namespace std;

#define all(x) x.begin(), x.end()

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(0);
  
  int n;
  cin >> n;

  int x1, y1, x2, y2, mx = 0, mn = 1e6;
  cin >> x1 >> y1;
  for(int i = 1; i < n; ++i){
    cin >> x2 >> y2;
    int dis = abs(x1 - x2) + abs(y1 - y2);
    mx = max(mx, dis);
    mn = min(mn, dis);
    x1 = x2;
    y1 = y2;
  }

  cout << mx << " " << mn << '\n';

  return 0;
}

